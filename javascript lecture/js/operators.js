// user input

const num1 = parseInt(prompt("Enter first number"));
const num2 = parseInt(prompt("Enter Second number"));

document.write("num1 :" + num1 + "<br>");
document.write("num2 :" + num2 + "<br>");

// string + string
// plus sign will use as a sign of concatenation
document.write("2" + "4" + "<br>");

// string + number
// plus sign will use as a sign of concatenation
document.write("2" + 4 + "<br>");

// number + number
// plus sign will use as a sign of addition
document.write(2 + 4 + "<br>");


let result = num1 + num2;
document.write("the addition is :" + result + "<br>");


// subtraction

result = num1 - num2;
document.write("the subtraction is :" + result + "<br>");

// multiplication

result = num1 * num2;
document.write("the multiplication is :" + result + "<br>");

// division

result = num1 / num2;
document.write("the division is :" + result + "<br>");

// modulus

result = num1 % num2;
document.write("the modulus is :" + result + "<br>");

